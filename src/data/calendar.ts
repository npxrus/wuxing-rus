// Текущий сезон на основе реальной даты
export function getCurrentSeason(): Season {
  const month = new Date().getMonth(); // 0-11
  
  if (month >= 2 && month <= 4) return SEASONS[0]; // Март-Май - Зелёный
  if (month >= 5 && month <= 7) return SEASONS[1]; // Июнь-Август - Алый
  if (month >= 8 && month <= 10) return SEASONS[2]; // Сентябрь-Ноябрь - Золотой
  if (month === 11 || month === 0) return SEASONS[3]; // Декабрь-Январь - Белый
  return SEASONS[4]; // Февраль - Чёрный
}

// Получить праздники для сезона
export function getHolidaysBySeason(seasonName: string): Holiday[] {
  return HOLIDAYS.filter(h => h.season === seasonName);
}